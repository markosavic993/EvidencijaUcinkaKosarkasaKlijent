    private void jbtnPotvrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPotvrdiActionPerformed
        String mail = jtxtMail.getText().trim();
        String ki = jtxtKI.getText().trim();
        String pass = jtxtPass.getText().trim();
        String potvrda = jtxtPotvrda.getText().trim();

        if (validanUnos(mail, ki, pass, potvrda)) {
            Korisnik k = new Korisnik(ki, pass, mail);

            toZahtev.setOperacija(Konstante.VRATI_SVE_KORISNIKE);
            KlijentKomunikacija.getInstance().posaljiZahtev(toZahtev);
            try {
                toOdgovor = KlijentKomunikacija.getInstance().primiOdgovor();
            } catch (IOException ex) {
                Logger.getLogger(FrmDodajKorisnika.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmDodajKorisnika.class.getName()).log(Level.SEVERE, null, ex);
            }
            List<Korisnik> lk = (List<Korisnik>) toOdgovor.getRezultat();
            if (lk.contains(k)) {
                jtxtKI.setBorder(null);
                jtxtPass.setBorder(null);
                jtxtMail.setBorder(null);
                jtxtPotvrda.setBorder(null);
                jtxtKI.requestFocusInWindow();
                jtxtMail.setText("");
                jtxtKI.setText("");
                jtxtPass.setText("");
                jtxtPotvrda.setText("");
                JOptionPane.showMessageDialog(this, "Korisnik već postoji!", "Greška", JOptionPane.WARNING_MESSAGE);
                return;
            }

            toZahtev.setOperacija(Konstante.DODAJ_KORISNIKA);
            toZahtev.setParametar(k);
            KlijentKomunikacija.getInstance().posaljiZahtev(toZahtev);

            try {
                toOdgovor = KlijentKomunikacija.getInstance().primiOdgovor();
            } catch (IOException ex) {
                Logger.getLogger(FrmDodajKorisnika.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmDodajKorisnika.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (toOdgovor.getIzuzetak() != null) {
                try {
                    throw (Exception) toOdgovor.getIzuzetak();
                } catch (Exception ex) {
                    Logger.getLogger(FrmDodajKorisnika.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            //Kontroler.getInstance().dodajKorisnika(k);
            JOptionPane.showMessageDialog(this, "Korisnik uspešno sačuvan!");
            this.dispose();
        }
    }//GEN-LAST:event_jbtnPotvrdiActionPerformed

